const printerCommand = {
	init: '\x1B' + '\x40',
	charsetLatin: '\x1B' + '\x74' + '\x27',
	center: '\x1B' + '\x61' + '\x01',
	left: '\x1B' + '\x61' + '\x30',
	right: '\x1B' + '\x61' + '\x32',
	boldOn: '\x1B' + '\x45' + '\x0D',
	small: '\x1B' + '\x4D' + '\x31',
	normal: '\x1B' + '\x4D' + '\x30',
	large: '\x1D' + '\x21' + '\x11',
	boldOff: '\x1B' + '\x45',
	cutPaper: '\x1B' + '\x69'
};

function printEscpos (dataPrint, vias, printer){
	let dados = [];
	dados.push({type:'raw', data: printerCommand.init}); //init
	dados.push({type:'raw', data: printerCommand.charsetLatin}); //charset

	for (let i = 0; i < dataPrint.length; i++)
		dados.push({type:'raw', data: dataPrint[i] + '\n'});

	dados.push({type:'raw', data: printerCommand.boldOn}); //bold
	dados.push({type:'raw', data: printerCommand.center}); //bold
	// dados.push({type:'raw', data: '\n\n' + 'DOCUMENTO NÃO FISCAL\n\n'});
	dados.push({type:'raw', data: '\n\n\n\n'});
	dados.push({type:'raw', data: printerCommand.cutPaper}); //cut paper

	connectAndPrint(dados, vias, printer);
}

function connectAndPrint(dados, vias, printer) {
	connect().then(function() {
		return print(dados, vias, printer);
	}).catch(printFail);             // so one catch is often enough for all promises
}

// connection wrapper
//  - allows active and inactive connections to resolve regardless
//  - try to connect once before firing the mimetype launcher
//  - if connection fails, catch the reject, fire the mimetype launcher
//  - after mimetype launcher is fired, try to connect 3 more times
function connect() {
	return new RSVP.Promise((resolve, reject) => {
		if (qz.websocket.isActive()) {// if already active, resolve immediately
			resolve();
		} else {
			// try to connect once before firing the mimetype launcher
			qz.websocket.connect().then(resolve).catch(() => {
				// if a connect was not succesful, launch the mimetime, try 3 more times
				window.location.assign("qz:launch");
				qz.websocket.connect({ retries: 2, delay: 1 }).then(resolve).catch(reject);
			});
		}
	}).catch((e) => console.log(e));
}

// print 
function print(dados, vias, printer) {
	let options =  { size: { width: 8.5, height: 11}, units: "in", density: "600", copies: vias };
	let config = qz.configs.create(printer, options);
	return qz.print(config, dados).then(printSuccess).catch(printFail);
}

// notify successful print
function printSuccess() {
	bootbox.alert("Impressão realizada com sucesso!");
}

// exception catch-all
function printFail(e) {
	bootbox.alert("Error: " + e);
	console.error(e);
}
